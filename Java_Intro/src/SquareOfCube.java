/* Write a program to make a square and cube of number.
*/public class SquareOfCube
{
	public static void main(String[] args)
	
	{
		double num = 4;
		
		// square of number 
		 double x = num * num;
		 
		// cube of number 
		 double y = num * num * num;
		 
		
		
		System.out.println("The square of number is : " + x);
		System.out.println("The cube of number is : " + y);
		
	}
}
